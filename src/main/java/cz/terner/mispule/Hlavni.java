    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        long timeStamp = System.currentTimeMillis();
        if (timeStamp % 2 == 0) {
            lblPopisek.setText(Status.class.getName() + " : " + timeStamp);
            for (Status st : Status.values()) {
                cbList.addItem(st.name());
            }
        } else {
            lblPopisek.setText(Cars.class.getName() + " : " + timeStamp);
            for (Cars car : Cars.values()) {
                cbList.addItem(car.name());
            }
        }
        
        btnGroup.add(rbAlfa);
        btnGroup.add(rbBeta);
        btnGroup.add(rbGamma);
    }//GEN-LAST:event_formWindowOpened

