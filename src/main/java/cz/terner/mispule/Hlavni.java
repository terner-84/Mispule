    @Override
    public void actionPerformed(ActionEvent e) {
        lblPopisek.setForeground(Color.red);
        lblPopisek.setBackground(Color.white);
        lblPopisek.setEnabled(true);
        jFormattedTextField1.setText("action event e");
        jFormattedTextField1.setDisabledTextColor(Color.red);
        if (e.getActionCommand().equals("Analyzuj")) {
            if (rbAlfa.isSelected()) {
                taOut.append("bla\n");
            } else if (rbBeta.isSelected()) {
                n.addColoredText(jTextPane1, cbList.getSelectedItem() + "\n", Color.blue, "Verdana", false);
            } else if (rbGamma.isSelected()) {
                n.addColoredText(jTextPane1, cbList.getSelectedItem() + "\n", Color.GREEN, "Courier New", true);
            }
        }
    }
    
    private class Neco {
        
        public void addColoredText(JTextPane pane, String text, Color color, String fontFamily, boolean boldFont) {
        StyledDocument doc = pane.getStyledDocument();

        Style style = pane.addStyle("Color Style", null);
        StyleConstants.setForeground(style, color);
        StyleConstants.setBold(style, boldFont);
        StyleConstants.setFontFamily(style, fontFamily);
        try {
            doc.insertString(doc.getLength(), text, style);
        } 
        catch (BadLocationException e) {
            e.printStackTrace();
        }           
        }
    }
    
